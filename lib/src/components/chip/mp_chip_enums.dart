/// Enum definitions for MPChip component
part of 'mp_chip.dart';

/// Size variants for MPChip
enum MPChipSize {
  /// Small chip (12px font)
  small,

  /// Medium chip (14px font)
  medium,

  /// Large chip (16px font)
  large,
}

/// Visual style variants for MPChip
enum MPChipVariant {
  /// Primary color chip
  primary,

  /// Secondary color chip (gray/light)
  secondary,

  /// Success color chip (green)
  success,

  /// Warning color chip (orange/amber)
  warning,

  /// Error color chip (red)
  error,

  /// Info color chip (blue/light blue)
  info,

  /// Outlined chip (transparent background with border)
  outlined,

  /// Filled chip (dark background)
  filled,
}
