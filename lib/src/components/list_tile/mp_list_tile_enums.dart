/// Size variants for list tiles
enum MPListTileSize {
  /// Compact list tile (48px height)
  small,

  /// Standard list tile (56px height)
  medium,

  /// Large list tile (72px height)
  large,
}

/// Visual style variants for list tiles
enum MPListTileVariant {
  /// Standard list tile appearance
  standard,

  /// Accent color for primary action elements
  primary,

  /// Subtle appearance for secondary items
  subtle,
}
