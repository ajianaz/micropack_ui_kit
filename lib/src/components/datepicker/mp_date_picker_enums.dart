part of 'mp_date_picker.dart';

/// The visual style variant of a date picker.
enum MPDatePickerVariant {
  /// Calendar-style date input field
  calendar,

  /// Button-style date picker
  button,

  /// Input field-style date picker
  input,
}

/// The size of a date picker.
enum MPDatePickerSize {
  /// Small size (compact)
  small,

  /// Medium size (default)
  medium,

  /// Large size (prominent)
  large,
}
