part of 'mp_slider.dart';

/// The visual style variant of a slider.
enum MPSliderVariant {
  /// Primary brand color
  primary,

  /// Secondary color
  secondary,

  /// Success color (green)
  success,

  /// Warning color (orange/amber)
  warning,

  /// Error color (red)
  error,

  /// Info color (blue)
  info,
}

/// The size of a slider.
enum MPSliderSize {
  /// Small size slider
  small,

  /// Medium size slider (default)
  medium,

  /// Large size slider
  large,
}
