/// Enum definitions for MPProgressBar component
part of 'mp_progress_bar.dart';

/// Size variants for MPProgressBar
enum MPProgressBarSize {
  /// Small progress indicator
  small,

  /// Medium progress indicator
  medium,

  /// Large progress indicator
  large,
}

/// Visual style variants for MPProgressBar
enum MPProgressBarVariant {
  /// Primary brand color
  primary,

  /// Secondary color
  secondary,

  /// Success color (green)
  success,

  /// Warning color (orange/amber)
  warning,

  /// Error color (red)
  error,

  /// Info color (blue)
  info,
}

/// Type of progress bar
enum MPProgressBarType {
  /// Linear/horizontal progress bar
  linear,

  /// Circular/spinning progress indicator
  circular,
}
