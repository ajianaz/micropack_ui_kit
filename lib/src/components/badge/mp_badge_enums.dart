library micropack_ui_kit.src.components.badge.mp_badge_enums;

/// The visual style variant of a badge.
enum MPBadgeVariant {
  /// Primary brand color
  primary,

  /// Secondary/dark color
  secondary,

  /// Success color (green)
  success,

  /// Warning color (orange/amber)
  warning,

  /// Error color (red)
  error,

  /// Info color (blue)
  info,

  /// Outlined style with transparent background
  outlined,

  /// Filled style with solid background
  filled,
}

/// The size of a badge.
enum MPBadgeSize {
  /// Small size badge
  small,

  /// Medium size badge (default)
  medium,

  /// Large size badge
  large,
}
