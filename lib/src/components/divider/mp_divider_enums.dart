part of 'mp_divider.dart';

/// Orientation variants for divider
enum MPDividerOrientation {
  /// Horizontal divider
  horizontal,

  /// Vertical divider
  vertical,
}

/// Thickness variants for divider
enum MPDividerThickness {
  /// Thin line (1px)
  thin,

  /// Medium line (2px)
  medium,

  /// Thick line (3px)
  thick,
}

/// Style variants for divider
enum MPDividerStyle {
  /// Solid line
  solid,

  /// Dashed line
  dashed,
}
