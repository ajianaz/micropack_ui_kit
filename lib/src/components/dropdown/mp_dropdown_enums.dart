/// Enum definitions for MPDropdown component
part of 'mp_dropdown.dart';

/// Size variants for MPDropdown
enum MPDropdownSize {
  /// Small dropdown (14px font)
  small,

  /// Medium dropdown (16px font)
  medium,

  /// Large dropdown (18px font)
  large,
}

/// Visual style variants for MPDropdown
enum MPDropdownVariant {
  /// Outlined dropdown with border
  outlined,

  /// Filled dropdown with background color
  filled,
}
